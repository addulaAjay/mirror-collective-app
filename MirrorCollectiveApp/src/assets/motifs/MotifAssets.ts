export const getMotifIcon = (motifId?: string): MotifIcon | undefined => {
  if (!motifId) return undefined;
  
  // Try exact match first
  const exactMatch = MOTIF_ICONS.find(m => m.id === motifId);
  if (exactMatch) return exactMatch;
  
  // try to match ignoring case
  const lowerId = motifId.toLowerCase();
  
  // Try adding/removing 'icon:' prefix
  const targetIds = [
    motifId,
    `icon:${motifId}`,
    motifId.replace(/^icon:/, ''),
    lowerId,
    `icon:${lowerId}`,
    lowerId.replace(/^icon:/, '')
  ];

  for (const tid of targetIds) {
    const match = MOTIF_ICONS.find(m => m.id === tid || m.id.toLowerCase() === tid);
    if (match) return match;
  }
  
  return undefined;
};
